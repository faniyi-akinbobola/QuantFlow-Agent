from openai import OpenAI
import re

def correctness_evaluator_llm(examples):
    """
    Evaluates the correctness of LLM outputs based on provided examples.
    
    Args:
        examples (list of dict): A list of dictionaries, each containing:
            - 'input': The input prompt given to the LLM.
            - 'output': The output generated by the LLM.
            - 'expected': The expected correct output for the given input.
    Returns:
        float: The correctness score as a percentage.
    """

    scores = []

    for example in examples:
        question = example["question"]
        output = example["output"]
        reference = example.get("reference", "")

        PROMPT = """
        You are a strict financial evaluator.
        Question:
        {question}
        Answer:
        {answer}
        Reference (if available):
        {reference}
        Task:
        Determine if the answer is factually correct and relevant.
        Return ONLY:
        score: 1 (correct) or 0 (incorrect)
        """

        response = OpenAI().chat.completions.create(
            model="gpt-4o-mini",
            messages=[
                {"role": "user", "content": PROMPT.format(question=question, answer=output, reference=reference)}
            ],
            temperature=0,
        )

        output = response.choices[0].message.content.strip()

        match = re.search(r'score:\s*(\d)', output.lower())
        score = int(match.group(1)) if match else 0
        scores.append(score)

    return sum(scores) / len(scores) * 100 if scores else 0